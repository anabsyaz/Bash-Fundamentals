#!/bin/bash

# This file will show how variables work in bash. 
# Variables are used to store data that can be used later in the script.
# To create a variable, you simply need to assign a value to it.
# The syntax for creating a variable is:
# variable_name=value
# There should be no spaces around the equal sign.
# For example, to create a variable called "name" and assign it the value "John", you would write:
name="John"
# To access the value of a variable, you need to use the dollar sign ($) before the variable name.
# For example, to print the value of the "name" variable, you would write:
echo $name
# This will output "John" to the terminal.
# Variables are case-sensitive, which means that "name" and "Name" are considered different variables.
# Variables are useful for storing data that can be used later in the script, such as user input, file names, or any other data that you want to use multiple times.
#
# Common variable types include:
# - String: A sequence of characters, such as "Hello, World!" and are enclosed in double quotes.
# - Integer: A whole number, such as 42 or -10.
# - Float: A number with a decimal point, such as 3.14 or -0.5.
# - Boolean: A value that can be either true or false, such as true or false.
# - Array: A collection of values, such as (1 2 3) or ("apple" "banana" "cherry").
